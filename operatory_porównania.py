# porównuja różne wartości i zwracają wartość True albo False
# ==  jest równe
# !=  nie jest równe
# >   jest większe
# <   jest mniejsze
# >=  jest większe lub równe
# <=  jest mniejsze lub równe

print("Ania" == "Ania")

print("Ania" != "Tomek")

print(1 > 3)

print(2 < 5)

print(2 >= 0)

print(0 >= 0)

print(1 <= 1)

print(1 <= 5)